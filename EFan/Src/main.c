const uint16_t SLOW_HALT = 300;
const uint16_t LINEAR_TEMP = 300;
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *h)
{
  if (&hadc == h)
  {
    uint16_t target = temperature[1];
    uint16_t current = temperature[0];
    static uint16_t previous = 0;
    TIM_OC_InitTypeDef sConfigOC = {0};

    if (current - SLOW_HALT < target)
    {
      // stop the pwm
      HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_1);
      return;
    }

    HAL_TIM_PWM_GetState(&htim3);

    // filter small bumper
    if (current > previous)
    {
      if (current - previous < 10)
      {
        return;
      }
    }
    else
    {
      if (previous - current < 10)
      {
        return;
      }
    }
    if (current > target)
    {
      uint16_t p = (current - target) * 70 / LINEAR_TEMP;
      if (p > 70)
        p = 70;
      p += 30;
      p *= 4;

      htim3.Instance->CCR1 = p;

      previous = current;
    }
  }
}
